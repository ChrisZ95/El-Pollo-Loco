/**
 * Represents a keyboard object to track key states.
 */
class Keyboard {
    /**
     * Represents the state of the left arrow key.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Represents the state of the right arrow key.
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * Represents the state of the down arrow key.
     * @type {boolean}
     */
    DOWN = false;

     /**
     * Represents the state of the space bar key.
     * @type {boolean}
     */
    SPACE = false;

    /**
     * Represents the state of the 'D' key.
     * @type {boolean}
     */
    D = false;
}